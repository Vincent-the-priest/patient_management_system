  <p>AIDS stands for acquired immunodeficiency syndrome. It is the most severe stage of HIV infection. HIV is a virus that attacks the body's immune system, which is the body's defense against infection. <br>When HIV infects the immune system, it gradually destroys CD4 cells, which are a type of white blood cell that helps the body fight off infections. As the number of CD4 cells decreases, the body becomes more vulnerable to infections and diseases!</p>
  <ul>
    <li>How is HIV transmitted?</li>
    <img src="ai1.jpg">
    <p>HIV can be transmitted through:</p>
<p>
    <b>Sexual contact:</b> HIV can be transmitted through vaginal, anal, or oral sex with an infected person.<br>
    <b>Sharing needles or syringes:</b> HIV can be transmitted through sharing needles or syringes that have been used by an infected person.<br>
    <b>From mother to child:</b> HIV can be transmitted from a mother to her child during pregnancy, childbirth, or breastfeeding.</p>
    <li>What are the symptoms of AIDS?</li>
    The symptoms of AIDS can vary depending on the stage of the infection. Some of the most common symptoms of AIDS include:
 <ul>
 <li>
    Fever</li>
   <li> Night sweats</li>
    <li>Swollen lymph nodes</li>
    <li>Fatigue</li>
    <li>Weight loss</li>
    <li>Diarrhea</li>
    <li>Pneumonia</li>
    </ul>
    <li>How is AIDS treated?</li>
    <img src="ai2.png">
    <p>There is no cure for AIDS, but there are treatments that can help control the virus and slow the progression of the disease. These treatments are called antiretroviral therapy (ART).<br> ART is a combination of drugs that work to prevent HIV from replicating in the body. With ART, people with HIV can live long and healthy lives.
</p><b>
<p>What can I do to protect myself from HIV?</p></b>
<ul>
There are several things you can do to protect yourself from HIV, including:

    <li><b>Get tested for HIV:</b> The only way to know for sure if you have HIV is to get tested. Testing is confidential and free or low-cost.</li>
    <li><b>Practice safe sex:</b> Use condoms consistently and correctly every time you have sex.</li>
    <li><b>Don't share needles or syringes:</b> If you inject drugs, never share needles or syringes with anyone else.</li>
    <li><b>Get vaccinated: </b>There is a vaccine that can protect you from certain types of sexually transmitted infections (STIs), which can make you more susceptible to HIV infection.</li>
</ul>
