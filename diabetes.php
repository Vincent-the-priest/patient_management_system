	<p><b>Diabetes</b> is a disease that occurs when your blood glucose, also called blood sugar is too high.</p> 
	<p>Glucose is your body's main source of energy. Your body can make glucose, but glucose also comes from the food you eat.</p> 
	<p><b>Insulin </b>is a hormone made by the pancreas that helps glucose get into your cells to be used for energy.</p><hr>
	<h3>Introduction</h3>
	<p><b>Diabetes</b> is a chronic health condition that affects how your body turns food into energy.</p> 
	<p>There are three main types of diabetes: type 1, type 2, and gestational diabetes.</p>
	<p><img src="diabetes1.webp">
	</div>
	<div>
		<p> <u><i>Type 1 diabetes:</i></u> <p>Type 1 diabetes is an autoimmune disease that causes your body to attack the cells in your pancreas that produce insulin.</p> 
		<p>Insulin is a hormone that helps your body use glucose for energy.</p> 
	<p>When your body doesn't produce insulin, glucose builds up in your blood.</p>
		</p>
	<p><u><i>Type 2 diabetes:</i></u> <p>Type 2 diabetes is the most common type of diabetes.</p>
		It occurs when your body doesn't produce enough insulin or doesn't use insulin effectively.<br>
		This can also cause glucose to build up in your blood.
	</p>
	<p> <u><i>Gestational diabetes:</i></u> <p>Gestational diabetes is a type of diabetes that develops during pregnancy.</p>
		It usually goes away after the baby is born, but women who have had gestational diabetes are at an increased risk of developing type 2 diabetes later in lif</p>
	</div>
	</div>
<p><h2>Symptoms of diabetes can include:</h2></p>
        <center><ul>
<li>Increased thirst</li>
        <li>Frequent urination</li>
        <li>Extreme hunger</li>
        <li>Unexplained weight loss</li>
        <li>Fatigue</li>
        <li>Blurred vision</li>
        <li>Slow-healing sores</li>
            <li>Frequent infections</li></ul></center>
<p>If you experience any of these symptoms, it is important to see your doctor to get tested for diabetes. <br>
    Early diagnosis and treatment can help prevent complications of diabetes, such as heart disease, stroke, blindness, and kidney failure.<br>

Treatment for diabetes depends on the type of diabetes you have.<br>
    Type 1 diabetes is treated with insulin injections. Type 2 diabetes can be treated with lifestyle changes, such as diet and exercise, and medication.<br> 
    Gestational diabetes can usually be controlled with diet and exercise.

There is no cure for diabetes, but it can be managed with proper treatment and lifestyle changes. With good management, people with diabetes can live long and healthy lives.
